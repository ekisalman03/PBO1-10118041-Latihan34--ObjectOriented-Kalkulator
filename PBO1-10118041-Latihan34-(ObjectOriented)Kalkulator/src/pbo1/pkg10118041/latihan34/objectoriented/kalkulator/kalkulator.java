public class kalkulator {
    public double value1;
    public double value2;

    public double tambahBilangan(){ return value1+value2;}
    public double kurangBiangan(){ return value1-value2;}
    public double kaliBilangan(){ return value1*value2;}
    public double bagiBilangan(){ return value1/value2;}
    public double sisaBilangan(){ return value1%value2;}
    
}
